"""
V A R I A B L E S
    Las variables son espacios de memoria que almacenan valores
    o datos de distintos tipos, y (como su nombre indica) pueden
    variar. Se crean en el momento que se les asigna un valor, por
    lo cual en Python no requerimos declararlas previamente.
"""
#primer valor
nombre = "Mally"
print(nombre)

#nuevo valor qe reemplaza el primero
nombre = "Samira"
print(nombre)

#tipo numerico
edad1 = 25
