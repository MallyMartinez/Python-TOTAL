#con input
ciudad = input("dime tu ciudad: ")
print("tu ciudad es: ",ciudad)

#concatenar
color1 = "azul"
color2 = "rojo"
frase = color1 +" y "+ color2
print(frase)

#concatenar con numeros
num1 = 50
