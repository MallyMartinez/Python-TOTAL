"""
P R I N T
    Declaración que al ejecutarse muestra (o imprime) en pantalla 
    el argumento que se introduce dentro de los paréntesis.
"""

""" 
    Mostrar texto
        Ingresamos entre comillas simples o dobles los 
        caracteres detexto que deben mostrarse en pantalla.
"""
print ("Hello, world!")


""" 
    Mostrar numeros
        Podemos entregarle a print() el número que debe mostrar,
        o una operación matemática a resolver. No empleamos 
        comillas en estos casos.
"""
print("25")

print(158+42)


# Combinados 
print("The result is:", 42)
